public class bitwiseOperations {
    public static void main(String[] args) {
        System.out.println("9|10 = " + (9|10)); //11
        System.out.println("9&10 = " + (9&10)); //8
        System.out.println("9^10 = " + (9^10)); //3
        System.out.println("~9 = " + ~9); //6
        System.out.println("9<<1 = " + (9<<1)); //18
        System.out.println("9<<2 = " + (9<<2)); //36
        System.out.println("9>>1 = " + (9>>1)); //4
        System.out.println("9>>2 = " + (9>>2)); //2
    }
}
